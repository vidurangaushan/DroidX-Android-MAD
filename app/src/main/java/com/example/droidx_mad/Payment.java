package com.example.droidx_mad;

public class Payment {

    private  String Name;
    private  String Amount;

    public Payment() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public String getAmount() {
        return Amount;
    }

    public void setAmount(String amount) {
        this.Amount = amount;
    }
}
